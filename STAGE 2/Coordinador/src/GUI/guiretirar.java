    private void BTNDEPOSITARActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNDEPOSITARActionPerformed
        Conector cliente = new Conector();
        String numcta = ctaRet.getText();
        float monto = Float.parseFloat(montoRet.getText());
        float saldoFinal = 0;
        String sfinal =null;
        cliente.conectar('C');
        try {
            saldoFinal = cliente.retiro(numcta,monto);
        } catch (SQLException ex) {
            Logger.getLogger(guiDeposito.class.getName()).log(Level.SEVERE, null, ex);
        }
        sfinal = Float.toString(saldoFinal);
        String mensaje ="Saldo final de la Cuenta:\n"+ sfinal;
        JOptionPane.showMessageDialog(null,mensaje);
        mensajeRet.setText(mensaje);
    }//GEN-LAST:event_BTNDEPOSITARActionPerformed

